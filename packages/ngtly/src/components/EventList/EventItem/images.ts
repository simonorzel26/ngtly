const getImageIndexFromEventId = (eventId: string): number => {
	let hash = 0;
	for (let i = 0; i < eventId.length; i++) {
		const char = eventId.charCodeAt(i);
		hash = (hash << 5) - hash + char;
		hash = hash & hash; // Convert to 32-bit integer
	}
	return Math.abs(hash) % images.length;
};

export const getImageUrlFromEventId = (eventId: string): string => {
	const index = getImageIndexFromEventId(eventId);

	// biome-ignore lint/style/noNonNullAssertion: because
	return images[index]!;
};
